import React, { useState, useEffect } from 'react';
import { motion } from 'framer-motion';

import { AppWrap, MotionWrap } from '../../wrapper';
import './About.css';
import { urlFor, client } from '../../client';

const About = () => {
  const [abouts, setAbouts] = useState([]);

  useEffect(() => {
    const query = '*[_type == "abouts"]';

    client.fetch(query).then((data) => {
      setAbouts(data);
    });
  }, []);

  return (
    <>
      <h2 className="head-text"> WELCOME TO <span>
         KYUSDA </span><span> CHURCH</span></h2>

   <p
    className="p-text"
    style={{ marginBottom: "20px", marginTop: "20px" , 
    textAlign:"center"}}
    >
    We always love to share the word of God and experience a quality
    time in Jesus feet. Our Services are well organised with fruitful
    messages and encouraging words to Strengthen and Teach feel Much
    welcome,we cant wait to see you and worship together.
        </p>
<p className="head-text" style={{textAlign:"center"}}>
  <span>Church Leaders</span></p>

      <div className="app__profiles">
        {abouts.map((about, index) => (
          <motion.div
            whileInView={{ opacity: 1 }}
            whileHover={{ scale: 1.1 }}
            transition={{ duration: 0.5, type: 'tween' }}
            className="app__profile-item"
            key={about.title + index}
          >
            <img src={urlFor(about.imgUrl)}
             alt={about.title} />
            <h2 className="bold-text" style={{ marginTop: 20 }}>{about.title}</h2>
            <p className="p-text" style={{ marginTop: 10 }}>{about.description}</p>
          </motion.div>
        ))}
      </div>
    </>
  );
};

export default AppWrap(
  MotionWrap(About, 'app__about'),
  'about',
  'app__whitebg',
);